package rtp

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestPacketRead(t *testing.T) {
	data := "\x80\x80\x92\xdb\x00\x00\x00\xa0\x34\x3d\xa9\x9b\xff\xff\xff\xff" +
		"\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff" +
		"\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\x7f\xff\xff\x7f" +
		"\xff\x7f\x7f\xff\xff\x7f\x7f\xff\x7f\xff\xff\xff\xff\xff\xff\xff" +
		"\xff\xff\xff\xff\xff\xff\xff\xff\xff\xfe\xff\xff\xfe\x7e\xfd\x7d" +
		"\xfd\x7e\x75\xfc\x73\x75\xfe\x71\x7b\x7e\x7a\xfc\xfd\xf9\xfb\xfb" +
		"\xf6\xff\xf9\xf8\x7c\xfa\xfd\x7d\xfc\xff\x7e\xfe\xfe\xfe\x7e\xfd" +
		"\x7e\x7d\xfe\x7c\x7c\x7d\x7a\x7b\x7b\x7c\x7d\x7f\xfd\xfb\xf8\xf5" +
		"\xf4\xf1\xf0\xf1\xf0\xf2\xf5\xf7\xfb\xff\x7a\x76\x71\x6e\x6d\x6b" +
		"\x6b\x6b\x6b\x6c\x6e\x70\x75\x7c\xf9\xf2\xeb\xe8\xe3\xdf\xde\xdb" +
		"\xe3\xdf\xe4\x7e\xf4\x6f\x62\x66\x5e\x5e\x5f\x60"

	rtp, err := Decode([]byte(data))
	assert.Nil(t, err)
	assert.NotNil(t, rtp)

	assert.Equal(t, 2, rtp.Version())
	assert.False(t, rtp.Padding())
}

